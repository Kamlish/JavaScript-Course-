// Math Object ‚ùì
// The JavaScript Math object is used to perform math operations on numbers.


    // Math.PI (property) - returns ratio of the circumference of a circle to its diameter.
    console.log(Math.PI); // output: 3.142..


    // Math.pow(x,y) - returns the value of x to the power y
    console.log(Math.pow(2,3)); // output: 8


    // Math.sqrt(x) - returns the value of x squared
    console.log(Math.sqrt(64)); // output: 8
    

   // Math.abs(x) - returns absolute (positive) value of x
    console.log(Math.abs(-11)); // output: 11


   // Math.ceil(x) - returns the value of x to rounded to the nearest bigger integer.
    console.log(Math.ceil(1.1)); // output: 2;


    // Math.floor(x) - returns the value of x to rounded to the nearest smaller integer.
    console.log(Math.floor(1.9)); // output: 1;


    // Math.round() - returns the value of x rounded to the nearest integer
    console.log(Math.round(1.1)); // output: 1
    console.log(Math.round(1.9)); // output: 2


    // Math.min() - can be used to find the minimum value in list of arguments
    console.log(Math.min(0,100,-3,40,5)); // output: -3


    // Math.max() - can be used to find the maximum value in list of arguments
    console.log(Math.max(0,100,-3,40,5)); // output: 100


    // Math.random() - returns a random number between 0 (inclusive) and 1 (exclusive)
    console.log(Math.random()); // output: 0.936
    console.log(Math.random()*10); // output: 6.43

    
    // Math.trunc() - returns the integer part of a number.
    console.log(Math.trunc(1.7)); // output: 1
    console.log(Math.trunc(-1.7)); // output: -1


    // üí° Concept: If the argument is a positive number, Math.trunc() is equivalent to Math.floor(),
    // ..... otherwise Math.trunc() is equivalent to Math.ceil()




    



